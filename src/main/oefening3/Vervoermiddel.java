package src.main.oefening3;

public class Vervoermiddel {
    private String merk;
    private String type;
    private String kleur;
    private int aantalWielen;

    public void setMerk(String merk) { this.merk = merk; }
    public String getMerk() { return this.merk; }

    public void setType(String type) { this.type = type; }
    public String getType() { return this.type; }

    public void setKleur(String kleur) { this.kleur = kleur; }
    public String getKleur() { return this.kleur; }

    public void setAantalWielen(int aantalWielen) { this.aantalWielen = aantalWielen; }
    public int getAantalWielen() { return this.aantalWielen; }

}
